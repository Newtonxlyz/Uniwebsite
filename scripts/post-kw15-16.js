async function main() {
  const author = await prisma.user.findFirst({ where: { email: 'admin@lvyz.org' } });
  if (!author) {
    console.error('找不到 admin 账号');
    process.exit(1);
  }

  for (const p of POSTS) {
    const existing = await prisma.post.findUnique({ where: { slug: p.slug } });
    if (existing) {
      console.log('已存在，跳过:', p.title);
      continue;
    }
    const post = await prisma.post.create({
      data: {
        ...p,
        status: 'PUBLISHED',
        allowComments: true,
        publishedAt: new Date(),
        authorId: author.id,
      },
    });
    console.log('✓ 录 blog:', post.title, '(', post.slug, ')');
  }
}

main()
  .catch((e) => { console.error('错误:', e); process.exit(1); })
  .finally(() => prisma.$disconnect());
